def lookup_error(rsp_cd: str) -> str:
    """응답코드로 오류 메시지 조회. 미등록 코드는 빈 문자열 반환."""
    for table in (
        PLATFORM_ERRORS_403,
        PLATFORM_ERRORS_400,
        PLATFORM_ERRORS_500,
        WEBSOCKET_ERRORS,
        SERVICE_ERRORS,
    ):
        if rsp_cd in table:
            return table[rsp_cd]
    return ""
